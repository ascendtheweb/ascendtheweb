// Add counter animation for pricing
function animateCounter(element, target, duration = 2000) {
    let start = 0;
    const increment = target / (duration / 16);
    
    function updateCounter() {
        start += increment;
        if (start < target) {
            element.textContent = Math.floor(start).toLocaleString();
            requestAnimationFrame(updateCounter);
        } else {
            element.textContent = target.toLocaleString();
        }
    }
    
    updateCounter();
}

// Trigger counter animation when pricing section is visible
const pricingObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
        if (entry.isIntersecting) {
            const amounts = entry.target.querySelectorAll('.amount');
            amounts.forEach(amount => {
                const value = parseInt(amount.textContent.replace(',', ''));
                animateCounter(amount, value);
            });
            pricingObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.5 });

const pricingSection = document.querySelector('.pricing');
if (pricingSection) {
    pricingObserver.observe(pricingSection);
} 